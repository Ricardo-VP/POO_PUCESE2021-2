package tipos_datos;

public class ejercicio1 {

	public static void main(String[] args) {
		// Declaracion de tiops de datos enteros
		long edad = 12;
		long edad1 = 40;
		
		// Declaracion de tipos de datos decimales
		double nota = 20.50;
		
		// Declaracion de tipos de datos caracter
		char op = 'a';
		
		// Declaracion de tipos de datos booleanos
		boolean decision = true;
		
		// Declaracion de tipos de datos Integer
		Integer numero = null;
		
		// Declaracion de tipo de datos string
		String palabra = "Hola mundo";
		
		// Constante
		final double PI = 3.141516;
		
		System.out.println("La edad es: " + edad);
		System.out.println("La edad de Carlos es: " + edad1);
		System.out.println("La nota de Carlos es: " + nota);
		System.out.println("La opcion elegida es: " + op);
		System.out.println("La decision es: " + decision);
		System.out.println("El valor del numero es: " + numero);
		System.out.println("La palabra es: " + palabra);
		System.out.println("El valor de PI es: " + PI);
	}

}
